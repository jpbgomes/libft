char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (c == s[i])
			return ((char *)s + i);
		i++;
	}
	if (s[i] == c)
		return ((char *)(s + 1));
	return (0);
}
