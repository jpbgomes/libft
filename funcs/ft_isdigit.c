#include <stdio.h>

int	ft_isdigit(unsigned char c)
{
	if (c > 47 && c < 58)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

int	main(void)
{
	printf("%d", ft_isdigit('0'));
	return (0);
}
