#include <stdio.h>

int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char myStr[] = "Arroz";
	printf("%d", ft_strlen(myStr));
}
